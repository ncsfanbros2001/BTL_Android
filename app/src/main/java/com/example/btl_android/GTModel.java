package com.example.btl_android;

public class GTModel {
    String area;
    String id;

    public GTModel(String area, String id) {
        this.area = area;
        this.id = id;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
